    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);
        Toolbar toolbar = (Toolbar) findViewById(R.id.toolbar);
        setSupportActionBar(toolbar);

        recyclerView=(RecyclerView)findViewById(R.id.Recyclerview);

        //set animation when while populating a list
        recyclerView.setItemAnimator(new DefaultItemAnimator());
        recyclerView.setHasFixedSize(false);

        //set 2 list item in single row
        recyclerView.setLayoutManager(new GridLayoutManager(this,2));

        //interface is used to perform actions when list item in recycler view is clicked
        Recycler_onclick_interface recycler_onclick_interface=new Recycler_onclick_interface() {
            @Override
            public void Onclicked(int position) {
                Log.d("Recycler Clicked",""+position);
            }
        };
        recyclerView.setAdapter(new Adapter(MainActivity.this,recycler_onclick_interface));

        FloatingActionButton fab = (FloatingActionButton) findViewById(R.id.fab);
        fab.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {
                Snackbar.make(view, "Replace with your own action", Snackbar.LENGTH_LONG)
                        .setAction("Action", null).show();
            }
        });
    }

